    public ArrayList<barang> getBarangList()
    {
       ArrayList<barang> barangList = new ArrayList<barang>();
       
       String query = "SELECT barang.kode_barang, barang.nama_barang,barang.stok_minimal,barang.harga_jual,barang.harga_beli, satuan.satuan FROM barang\n" +
"JOIN satuan ON barang.id_satuan = satuan.id_satuan";
       
       Statement st;
       ResultSet rs;
       
       try{
           st = con.createStatement();
           rs = st.executeQuery(query);
           
           barang Barang;           
           while(rs.next()){
               
               Barang = new barang(rs.getInt("kode_barang"),rs.getString("nama_barang"), 
                       rs.getString("satuan"), rs.getInt("stok_minimal"), 
                       rs.getDouble("harga_jual"), rs.getDouble("harga_beli"));
               barangList.add(Barang);
           }        
       }catch(Exception e){
           e.printStackTrace();
       }       
       return barangList;
    }
    
    
    public void show_barang_inJtable(){
        
        ArrayList<barang> list = getBarangList();
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        Object[] row = new Object[6];
        
        for(int i=0; i < list.size(); i++){
            row[0] = list.get(i).getKodeBarang();
            row[1] = list.get(i).getNamaBarang();
            row[2] = list.get(i).getSatuan();
            row[3] = list.get(i).getstokMinimal();
            row[4] = list.get(i).getHargaJual();
            row[5] = list.get(i).getHargaBeli();
            
            model.addRow(row);
        }       
    }

