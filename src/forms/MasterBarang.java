    private void updateCombo(){
    String query = "SELECT * FROM satuan";
    PreparedStatement ps;
    ResultSet rs;
    try{
    ps = con.prepareStatement(query);
    rs= ps.executeQuery();
    while(rs.next()){
    cbJenis.addItem(rs.getString("satuan"));
    }
    
    }catch(Exception ex){
    JOptionPane.showMessageDialog(null, "Data Kosong", "Information", JOptionPane.ERROR_MESSAGE);
    }
    
    }
        
    public void executeSQLQuery(String query, String pesan)
    {
        Statement st;
        
        try{
           st = con.createStatement();
            ImageIcon iconSucceed = new ImageIcon("src/images/checked.png");
            ImageIcon iconFailed = new ImageIcon("src/images/remove.png");
           if(st.executeUpdate(query) == 1){
               
               //refresh jtable data
               DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
               model.setRowCount(0);
               show_barang_inJtable();
               
                JOptionPane.showMessageDialog(null, "Data " +pesan+" Succesfully", "Information", JOptionPane.INFORMATION_MESSAGE, iconSucceed);
           }
           else{
               JOptionPane.showMessageDialog(null, "Data Not" +pesan, "Information", JOptionPane.INFORMATION_MESSAGE, iconFailed);          
           }
                 
       }catch(Exception e){
           e.printStackTrace();
       }        
    }
    
